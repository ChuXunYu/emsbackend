package com.dne.ems.model.enums;

/**
 * Defines the gender of a user.
 *
 * @version 1.1
 * @since 2025-06-16
 */
public enum Gender {
    /**
     * Represents the male gender.
     */
    MALE,

    /**
     * Represents the female gender.
     */
    FEMALE,

    /**
     * Represents cases where the gender is not specified or is unknown.
     */
    UNKNOWN
} 